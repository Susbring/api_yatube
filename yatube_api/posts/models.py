from django.contrib.auth import get_user_model
from django.db import models

User = get_user_model()


GROUP_TITLE_MAX_LENGTH = 200
POST_IMAGE_UPLOAD_TO = 'posts/'
COMMENT_CREATED_VERBOSE_NAME = 'Дата добавления'
POST_PUB_DATE_VERBOSE_NAME = 'Дата публикации'


class Group(models.Model):
    title = models.CharField(max_length=GROUP_TITLE_MAX_LENGTH)
    slug = models.SlugField(unique=True)
    description = models.TextField()

    class Meta:
        verbose_name = 'Группа'
        verbose_name_plural = 'Группы'

    def __str__(self):
        return self.title


class Post(models.Model):
    text = models.TextField()
    pub_date = models.DateTimeField(
        POST_PUB_DATE_VERBOSE_NAME, auto_now_add=True
    )
    author = models.ForeignKey(
        User, on_delete=models.CASCADE, related_name='posts'
    )
    image = models.ImageField(
        upload_to=POST_IMAGE_UPLOAD_TO, null=True, blank=True
    )  # поле для картинки
    group = models.ForeignKey(
        Group, on_delete=models.SET_NULL,
        related_name='posts', blank=True, null=True
    )

    class Meta:
        verbose_name = 'Пост'
        verbose_name_plural = 'Посты'
        ordering = ['-pub_date']

    def __str__(self):
        return self.text


class Comment(models.Model):
    author = models.ForeignKey(
        User, on_delete=models.CASCADE, related_name='comments'
    )
    post = models.ForeignKey(
        Post, on_delete=models.CASCADE, related_name='comments'
    )
    text = models.TextField()
    created = models.DateTimeField(
        COMMENT_CREATED_VERBOSE_NAME, auto_now_add=True, db_index=True
    )

    class Meta:
        verbose_name = 'Комментарий'
        verbose_name_plural = 'Комментарии'
        ordering = ['-created']

    def __str__(self):
        return self.text
